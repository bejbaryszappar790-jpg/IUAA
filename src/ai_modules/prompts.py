SYSTEM_PROMPT = """
ВАЖНО: ВАШ ОТВЕТ ДОЛЖЕН БЫТЬ ТОЛЬКО НА РУССКОМ ЯЗЫКЕ. НЕ ИСПОЛЬЗУЙТЕ АНГЛИЙСКИЕ ИЛИ КИТАЙСКИЕ ИЕРОГЛИФЫ.

Вы — эксперт приемной комиссии InVision U. Ваша цель — выявить будущих лидеров и предпринимателей Казахстана.

ОСНОВНЫЕ ПРАВИЛА:
- Не поощряйте вычурный язык. Определяйте ИИ-генерацию или шаблонные фразы.
- Если конкретных доказательств опыта нет — ставьте НИЗКИЙ балл.
- Будьте консервативны и скептичны. 

ИНСТРУКЦИЯ ПО ОЦЕНКЕ:
1. Лидерский потенциал (0-10): Ищите реальные действия, а не слова "Я лидер".
2. Ориентация на рост (0-10): Ищите примеры преодоления трудностей.
3. Подлинность (0-10): Если текст шаблонный или "слишком идеальный" — балл не выше 5.

ИНСТРУКЦИЯ ПО СЕРТИФИКАТАМ:
- Используйте данные из блока "ТЕХНИЧЕСКИЙ РАЗБОР СЕРТИФИКАТА".
- Если QR найден и ссылка валидна — подтвердите подлинность.
- Если сертификат просрочен — укажите это как минус к актуальности навыков.

ФОРМАТ ОТВЕТА (СТРОГО):

Эссе:
Лидерство: X/10 (Краткое объяснение)
Рост: X/10 (Краткое объяснение)
Подлинность: X/10 (Краткое объяснение)
Заключение: [Сильные и слабые стороны кандидата]

Подлинность сертификатов:
[Ваш технический вердикт на основе предоставленных данных]

СТРОГО РУССКИЙ ЯЗЫК.
"""

def format_certificate(cert):
    """
    Преобразует технический словарь из cert_val.py в текст для ИИ.
    """
    if not cert:
        return "ТЕХНИЧЕСКИЙ РАЗБОР СЕРТИФИКАТА: Данные не предоставлены."

    qr_status = "✅ Найден" if cert.get("qr_found") else "❌ Не найден (или файл не является изображением)"
    link_status = "✅ Валидна" if cert.get("link_valid") else f"❌ Ошибка ({cert.get('link_message')})"
    
    expiry = cert.get("expiry", {})
    is_expired = expiry.get("is_expired", False)
    exp_status = "⚠️ ПРОСРОЧЕН" if is_expired else "✅ Действителен"
    days_left = expiry.get("days_left", 0)

    return f"""
ТЕХНИЧЕСКИЙ РАЗБОР СЕРТИФИКАТА:
- Наличие QR-кода: {qr_status}
- Ссылка из QR: {cert.get("qr_url", "Отсутствует")}
- Статус проверки ссылки: {link_status}
- Срок действия: {exp_status} (Осталось дней: {days_left})
"""

def build_full_prompt(essay_text, cert_result=None):
    """
    Собирает финальный промпт для отправки в LLM.
    """
    cert_section = format_certificate(cert_result)

    # Если вместо эссе пришла заглушка об изображении
    if "Image file detected" in essay_text:
        context_note = "\nЗАМЕТКА: Кандидат отправил только файл сертификата. Оцените подлинность по техническим данным.\n"
    else:
        context_note = ""

    return f"""
{SYSTEM_PROMPT}

{context_note}

ЭССЕ КАНДИДАТА (ИЛИ ТЕКСТ ИЗ ФАЙЛА):
{essay_text}

{cert_section}
"""