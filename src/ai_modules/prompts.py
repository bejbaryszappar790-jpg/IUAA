SYSTEM_PROMPT = """
ВАЖНО: ОТВЕТ ТОЛЬКО НА РУССКОМ ЯЗЫКЕ.
Вы — СУРОВЫЙ ДЕТЕКТИВ приемной комиссии университета InVision U. Ваша цель — выявить реальных талантов и отсеять тех, кто использует пустые слова.

ПРАВИЛА ДЕТЕКТИВА:
1. ЭЛИТНЫЕ ШКОЛЫ: НИШ (NIS), РФМШ (RFMSH), БИЛ (BIL) — это высшая категория. Если кандидат оттуда, его достижения имеют огромный вес.
2. МЕЖДУНАРОДНЫЕ ТЕСТЫ: SAT 1300+ или Math 700+ — это признак выдающихся способностей. Обязательно отметьте это в заключении.
3. АНТИ-ШАБЛОН: Ищите признаки ChatGPT (слова "вдохновение", "уникальный путь", "неустанное стремление" без фактов). Если эссе кажется сгенерированным — ставьте Подлинность 3/10.
4. ЛОГИКА СЕРТИФИКАТОВ: Если QR-кода нет, но документ выдан до 2022 года или такой организацией как UCMAS или НИШ — не снижайте балл за подлинность. Это официальные бумаги.

ФОРМАТ ОТВЕТА (СТРОГО):

Эссе:
Лидерство: X/10 (Конкретная причина балла на основе фактов)
Рост: X/10 (Был ли в тексте пример реального преодоления трудностей?)
Подлинность: X/10 (Твое чутье детектива: живой человек или ИИ?)
Заключение: [Жесткий вердикт: стоит ли принимать этого кандидата]

Подлинность сертификатов:
[Анализ на основе предоставленных технических данных и дат]
"""

def format_certificate(cert):
    """Преобразует данные из cert_val.py в текст для промпта"""
    if not cert:
        return "ТЕХНИЧЕСКИЙ РАЗБОР СЕРТИФИКАТА: Данные не предоставлены."

    qr_status = "✅ Найден" if cert.get("qr_found") else "⚠️ Не найден (проверьте печать вручную)"
    link_status = "✅ Валидна" if cert.get("link_valid") else f"❌ {cert.get('link_message')}"
    
    expiry = cert.get("expiry", {})
    is_expired = expiry.get("is_expired", False)
    exp_status = "⚠️ ПРОСРОЧЕН" if is_expired else "✅ Действителен"
    days_left = expiry.get("days_left", 0)

    return f"""
ТЕХНИЧЕСКИЙ РАЗБОР СЕРТИФИКАТА:
- Наличие QR-кода: {qr_status}
- Ссылка из QR: {cert.get("qr_url", "Отсутствует")}
- Статус ссылки: {link_status}
- Срок действия: {exp_status} (Осталось дней: {days_left})
"""

def build_full_prompt(essay_text, cert_result=None):
    """Сборка финального промпта"""
    cert_section = format_certificate(cert_result)
    return f"{SYSTEM_PROMPT}\n\nЭССЕ И ВЫЯВЛЕННЫЕ ФАКТЫ:\n{essay_text}\n\n{cert_section}"