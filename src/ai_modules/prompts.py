def format_certificate(cert):
    return f"""
Type: {cert.get('type')}
Valid: {cert.get('valid')}
Expired: {cert.get('expired')}
Score: {cert.get('score')}
Confidence: {cert.get('confidence', 'N/A')}
"""


def build_full_prompt(essay_text, cert_result=None):
    cert_section = ""

    if cert_result:
        cert_section = f"""
{format_certificate(cert_result)}
"""

    return f"""
{SYSTEM_PROMPT}

ЭССЕ КАНДИДАТА:
{essay_text}

{cert_section}
"""